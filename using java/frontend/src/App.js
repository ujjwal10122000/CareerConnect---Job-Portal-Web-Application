import React from 'react';
import { BrowserRouter as Router, Route, Routes } from 'react-router-dom';
import './App.css';
import Register from './components/Register';
import Login from './components/Login';
import Navbar from './components/Navbar';
import HomePage from './components/Home';
import About from './pages/About';
import Contact from './pages/Contact';
import Footer from './components/Footer';
import JobsList from './components/JobsList';
import JobSeekerProfile from './components/JobSeekerProfile';
import EditJobSeekerProfile from './components/EditJobSeekerProfile';
import AddProject from './components/AddProject';
import EditProject from './components/EditProject';
import JobSeekerProjects from './components/JobSeekerProjects';
import EmployerEditProfile from './components/EmployerEditProfile';
import EmployerJobs from './components/EmployerJobs';
import CreateJob from './components/CreateJob';
import EditJob from './components/EditJob';
import ApplicationStatus from './components/ApplicationStatus';
import ViewApplications from './components/ViewApplications';
import JobSeekerProfileView from './components/JobSeekerProfileView';
import EmployerProfile from './components/EmloyerProfile';

function App() {
  const user = JSON.parse(localStorage.getItem('user'));
  return (
    <Router>
      <div className="App">
        <Navbar />
        <Routes>
          {/* Common Routes */}
          <Route path="/login" element={<Login />} />
          <Route path="/register" element={<Register />} />
          <Route path="/" element={<HomePage />} />
          <Route path="/about" element={<About />} />
          <Route path="/contact" element={<Contact />} />
          <Route path="/jobs" element={<JobsList />} />
          
          
          <Route path="/profile" element={ user && user.role === "JobSeeker" ? (<JobSeekerProfile />) : (<EmployerProfile />)} />
          
          
          {/* Job Seeker Routes */}
          {/* <Route path="/profile" element={<JobSeekerProfile />} /> */}
          <Route path="/edit-profile" element={<EditJobSeekerProfile />} />
          <Route path="/add-project" element={<AddProject />} />
          <Route path="/edit-project/:projectId" element={<EditProject />} />  {/* Route for editing project with projectId */}
          <Route path="/projects" element={<JobSeekerProjects />} />
          <Route path="/my-applications" element={<ApplicationStatus />} />
          <Route path="/job-seeker-profile/:jobSeekerId" element={<JobSeekerProfileView />} />
          <Route path="/add-project" element={<AddProject />} />
          <Route path="/jobslist" element={<JobsList />} />

          {/* Employer Routes */}
          <Route path="/edit-employer-profile/:employerId" element={<EmployerEditProfile />} />
          <Route path="/view-applications/:jobId" element={<ViewApplications />} />
          <Route path="/my-jobs" element={<EmployerJobs />} />
          <Route path="/create-job" element={<CreateJob />} />
          <Route path="/edit-job/:jobId" element={<EditJob />} />

          {/* Fallback Route */}
          {/* <Route path="*" element={<NotFound />} /> */}
        </Routes>
        <Footer />
      </div>
    </Router>
  );
}

export default App;
